    /**
     * Gets and returns system information.
     * @return 
     */
    private String getSystemInfo() {
        String returnVal = "";
        
        String osArch = System.getProperty("os.arch");
        String osName = System.getProperty("os.name");
        String osVer = System.getProperty("os.version");
        String javaHome = System.getProperty("jva.home");
        String javaVer = System.getProperty("java.version");
        String userHome = System.getProperty("user.home");
        String userName = System.getProperty("user.name");
        
        String info = "OS Props\t\tJava Props\t\tUser Props\n" +
                            "OS Arch: " + osArch + "\tJava Home: " + javaHome + "\tUser Home: " + userHome + "\n" +
                            "OS Name: " + osName + "\tJava Ver: " + javaVer + "\tUser Name: " + userName + "\n" +
                            "OS Ver: " + osVer;
        
        returnVal = info;
        
        return returnVal;
    }

